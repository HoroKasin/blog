package Identifier;

public class Identifier {

	public static void main(String[] args) {
		
		//The Java is case sensitive when naming identifiers.
		int age = 27;
		int Age = 28;
		int aGE = 29;
		System.out.println(age);
		System.out.println(Age);
		
		//Do not start distinguished names with numbers
		int number4 = 4;
		int num4ber = 4;
		
		//The identifier must not contain spaces
//		int phone number = 12345678; (X)
		int phoneNumber = 12345678;
		
		//unable as identifier
//		int if = 4;
//		int while = 4;
	}
}






