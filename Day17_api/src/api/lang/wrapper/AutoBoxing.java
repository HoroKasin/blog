package api.lang.wrapper;

public class AutoBoxing {

	public static void main(String[] args) {
		
		//AutoBoxing
		//recommended auto-boxing function from version 10 of Java.
		Integer val1 = 200;
		Double val2 = 3.14159;
		
		//AutoUnboxing
		int a = val1;
		double b = val2 + 5.4;
		
		System.out.println(a + b);
		

	}

}



